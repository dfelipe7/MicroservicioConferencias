package co.edu.unicauca.conferences.dto;

/**
 *
 * @author Unicauca
 */

public class UserDTO {
    private Long id;
    private String role;

    // Constructor
    public UserDTO(Long id, String role) {
        this.id = id;
        this.role = role;
    }

    // Getters y Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
